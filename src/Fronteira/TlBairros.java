    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed

        if (jTable1.getSelectedRow() >= 0) {
            TlAlterarBairros alt = new TlAlterarBairros(this);

            alt.lbCod.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 0) + "");
            alt.txtNome.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1) + "");
            alt.txtTaxa.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2) + "");

            alt.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela");
        }

    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnRemoverBairroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverBairroActionPerformed

        if (jTable1.getSelectedRow() >= 0) {
            int Cod = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0) + "");

            PerBairros pa = new PerBairros();
            pa.DeletarBairros(Cod);

            PreencherTabela();
            txtBairro.setText("");
            txtTaxa.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela para remover!");
        }

    }//GEN-LAST:event_btnRemoverBairroActionPerformed

    private void btnAdicionarBairroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarBairroActionPerformed

        if (txtBairro.getText().isEmpty() || txtTaxa.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Um dos campos est√° vazia, verifique novamente!");

        } else {
            ClsBairros addBairros = new ClsBairros();

            addBairros.setNome(txtBairro.getText());
            addBairros.setPreco(Double.parseDouble(txtTaxa.getText()));

            PerBairros.InserirBairros(addBairros);

            PreencherTabela();
            txtBairro.setText("");
            txtTaxa.setText("");
        }
    }//GEN-LAST:event_btnAdicionarBairroActionPerformed

    public void PreencherTabela() {
        //vai limpar a tabela
        int t = vTabela.getRowCount();
        for (int i = 0; i < t; i++) {
            vTabela.removeRow(0);
        }

        PerBairros perbairros = new PerBairros();
        ArrayList<ClsBairros> bairros = PerBairros.TodosBairros();

        //vai preencher a tabela
        for (ClsBairros q : bairros) {
            String linha[] = new String[3];

            linha[0] = q.getCod() + "";
            linha[1] = q.getNome();
            linha[2] = q.getPreco() + "";
            vTabela.addRow(linha);
        }

    }

