    private void btnVitAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVitAdicionarActionPerformed

        if (txtVitTipo.getText().isEmpty() || txtVitNome.getText().isEmpty() || txtPreco.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Um dos campos est√° vazia, verifique novamente!");

        } else {

            ClsVitamina addVitamina = new ClsVitamina();

            addVitamina.setTipo(txtVitTipo.getText());
            addVitamina.setNome(txtVitNome.getText());
            addVitamina.setPreco(Double.parseDouble(txtPreco.getText()));

            PerVitamina.InserirVitamina(addVitamina);

            PreencherTabela();
            txtVitTipo.setText("");
            txtVitNome.setText("");
            txtPreco.setText("");
        }
    }//GEN-LAST:event_btnVitAdicionarActionPerformed

    private void btnVitRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVitRemoverActionPerformed

        if (jTable1.getSelectedRow() >= 0) {
            int Cod = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0) + "");

            PerVitamina pa = new PerVitamina();
            pa.DeletarVitamina(Cod);

            PreencherTabela();
            txtVitTipo.setText("");
            txtVitNome.setText("");
            txtPreco.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela para remover!");
        }

    }//GEN-LAST:event_btnVitRemoverActionPerformed

    private void btnVitAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVitAlterarActionPerformed

        if (jTable1.getSelectedRow() >= 0) {
            TlAlterarVitamina alt = new TlAlterarVitamina(this);

            alt.lbCod.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 0) + "");
            alt.txtTipo.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1) + "");
            alt.txtNome.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2) + "");
            alt.txtValor.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 3) + "");

            alt.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela");
        }

    }//GEN-LAST:event_btnVitAlterarActionPerformed

    public void PreencherTabela() {
        //vai limpar a tabela
        int t = vTabela.getRowCount();
        for (int i = 0; i < t; i++) {
            vTabela.removeRow(0);
        }

        PerVitamina pervitamina = new PerVitamina();
        ArrayList<ClsVitamina> vitamina = PerVitamina.TodasVitaminas();

        //vai preencher a tabela
        for (ClsVitamina q : vitamina) {
            String linha[] = new String[4];

            linha[0] = q.getCod() + "";
            linha[1] = q.getTipo();
            linha[2] = q.getNome();
            linha[3] = q.getPreco() + "";
            vTabela.addRow(linha);
        }

    }

